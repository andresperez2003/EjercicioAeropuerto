package Models;

/**
 *
 * @author User
 */
public class Ultraligero extends Aeronave {
    private String id;
    private double peso;

    public Ultraligero(String id, double peso, String placa, String color, int capacidad, String lugarCreacion) {
        super(placa, color, capacidad, lugarCreacion);
        this.id = id;
        this.peso = peso;
    }

    
    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the peso
     */
    public double getPeso() {
        return peso;
    }

    /**
     * @param peso the peso to set
     */
    public void setPeso(double peso) {
        this.peso = peso;
    }
}
